<h2>Accès au matériel</h2>
<img src="./img/HTML5_Device_Access_256.png" />
<p>
	Ne serait-ce qu'avec l'API de géolocalisation, les applications Web peuvent fournir des fonctionnalités et
	une expérience utilisateur plus riche, au fait des possibilités du matériel. Des innovations stupéfiantes
	pour accéder au matériel sont développées et implémentées, depuis l'accès à de nouvelles entrées audio/vidéo
	au moyen de microphones et de caméras jusqu'à l'accès à des données locales comme les contacts et les événements,
	et même la détection de l'orientation.
</p>